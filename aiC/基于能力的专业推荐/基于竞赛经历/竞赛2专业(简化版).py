# 学群的列表
g=g_a.keys()

# 计算某竞赛某奖项的含金量，范围为1~5
def p_point(c_name, p_name):
    if p_name=='一等奖':
        point=5
    elif p_name=='二等奖':
        point=3
    elif p_name=='三等奖':
        point=1

    return point

exp1=[['全国中学生数学竞赛','二等奖'],['中国青少年机器人竞赛','二等奖']]
exp2=[['全国中学生科普科幻作文大赛','一等奖'],['全国青少年科学影像大赛','二等奖']]

# 根据竞赛计算各个学群的得分
def group_score(exp):
    g_s={}
    for e in exp:
        c_name=e[0]
        p_name=e[1]
        a_list=c_a[c_name]
        score=p_point(c_name,p_name)

        for ability in a_list:
            for group in g:
                g_alist=g_a[group]
                if ability in g_alist:
                    if group in g_s:
                        g_s[group]+=score
                    else:
                        g_s[group]=0
    return g_s

# 计算各学群的推荐度
def recommand(g_s):
    g_r={}
    m=max(g_s,key=g_s.get)
    m_score=g_s[m]
    for group in g_s.keys():
        g_r[group]=g_s[group]/m_score

    return g_r

# 展示各学群推荐度
def show(g_r):
    result=sorted(g_r.items(), key=lambda asd:asd[1], reverse=True)
    for r in result:
        print(r[0],'  推荐度：',r[1])

print('竞赛经历',exp2)
show(recommand(group_score(exp2)))