'''
选择是否为双一流大学
'''
def Double_first_class_select():
    iwanna_class=input("请输入是否需要是双一流大学：(y/n)")
    df=pd.read_csv('./colle_shrink.csv')
    class_name=df.loc[:,['大学','是否为双一流']]
    # 判断输入合法性
    while True:
        try:
            if iwanna_class=='y':
                new_class=class_name[class_name['是否为双一流']=='是']
            elif iwanna_class=='n':
                new_class=class_name
            break
        except:
            print('您输入的内容不规范，请重新输入')

        return new_class
    return new_class


