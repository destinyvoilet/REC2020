def first_class_tier():
    iwanna_tier=input("请输入是否只想去一本：(y/n)")
    df = pd.read_csv('./colle_shrink.csv')
    tier_name = df.loc[:, ['大学', '本数']]
    # 判断输入合法性
    while True:
        try:
            if iwanna_tier == 'y':
                new_tier = tier_name[tier_name['本数'] == '一本']

            elif iwanna_tier=='n':
                new_tier = tier_name
            break
        except:
            print('您输入的内容不规范，请重新输入')
    return new_tier
